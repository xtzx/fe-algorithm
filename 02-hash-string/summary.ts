/**
 * 🧩 变形题识别技巧
 *
 * 1. 三数之和 = 固定一个数 + 两数之和
 *
 * 2. 四数相加 II = 两两分组 + 两数之和
 *    nums1[i] + nums2[j] + nums3[k] + nums4[l] = 0
 *    → (nums1[i] + nums2[j]) + (nums3[k] + nums4[l]) = 0
 *
 * 3. 找所有异位词 = 滑动窗口（固定大小）+ 字符计数
 *
 * 4. 最小覆盖子串 = 滑动窗口（可变大小）+ 字符计数
 *
 * 5. 最长连续序列 = 哈希表 + 只从起点开始数
 *
 * 6. 前 K 个高频 = 哈希计数 + 桶排序/堆
 */

