/**
 * 🧩 N 数之和系列
 *
 * ┌─────────────────────────────────────────────────────────────┐
 * │  两数之和（无序）→ 哈希表 O(n)                              │
 * │  两数之和（有序）→ 对撞指针 O(n)                           │
 * │  三数之和 → 排序 + 固定一个 + 对撞指针 O(n²)               │
 * │  四数之和 → 排序 + 固定两个 + 对撞指针 O(n³)               │
 * │  N 数之和 → 递归降维，直到两数之和 O(n^(N-1))             │
 * └─────────────────────────────────────────────────────────────┘
 *
 * 🧩 原地修改系列
 *
 * ┌─────────────────────────────────────────────────────────────┐
 * │  移动零 → 快慢指针，然后填充 0                              │
 * │  移除元素 → 快慢指针，只保留不等于 val 的                   │
 * │  删除有序数组中的重复项 → 快慢指针，比较相邻                │
 * │  删除有序数组中的重复项 II → 比较 nums[slow-1] 和 nums[fast]│
 * │  轮转数组 → 三次反转                                       │
 * └─────────────────────────────────────────────────────────────┘
 *
 * 🧩 滑动窗口系列
 *
 * ┌─────────────────────────────────────────────────────────────┐
 * │  最长（如无重复最长子串）→ 扩张时更新答案                   │
 * │  最短（如长度最小的子数组）→ 收缩时更新答案                 │
 * │  恰好等于 K → 转化为 >=K 的个数 - >=K+1 的个数              │
 * │  有负数 → 不能用滑动窗口，改用前缀和                        │
 * └─────────────────────────────────────────────────────────────┘
 *
 * 🧩 前缀和系列
 *
 * ┌─────────────────────────────────────────────────────────────┐
 * │  区间和查询 → 基础前缀和                                    │
 * │  和为 K 的子数组 → 前缀和 + 哈希表（找 prefix[i] = prefix[j]-K）│
 * │  和可被 K 整除 → 前缀和 + 余数哈希                          │
 * │  0 和 1 个数相等 → 0 变成 -1，转化为和为 0                  │
 * │  除自身以外的乘积 → 前缀积 + 后缀积                         │
 * └─────────────────────────────────────────────────────────────┘
 */

