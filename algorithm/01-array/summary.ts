/**
 * 📌 1. 对撞指针
 *
 * 适用条件：
 * - 数组有序（或可以排序）
 * - 需要找两个数满足某种条件
 *
 * 模板：
 * ```
 * let left = 0, right = n - 1;
 * while (left < right) {
 *   if (满足条件) return result;
 *   else if (需要增大) left++;
 *   else right--;
 * }
 * ```
 *
 * 核心思想：
 * - 从两端向中间逼近
 * - 根据条件判断移动哪个指针
 *
 * 典型题目：
 * - 两数之和 II（有序数组）
 * - 三数之和（排序 + 对撞）
 * - 盛最多水的容器（贪心 + 对撞）
 * - 接雨水（预处理 / 对撞）
 */

/**
 * 📌 2. 快慢指针
 *
 * 适用条件：
 * - 原地修改数组
 * - O(1) 空间复杂度
 * - 需要分区（保留/删除）
 *
 * 模板：
 * ```
 * let slow = 0;
 * for (let fast = 0; fast < n; fast++) {
 *   if (需要保留) {
 *     nums[slow] = nums[fast];
 *     slow++;
 *   }
 * }
 * return slow; // 新数组长度
 * ```
 *
 * 核心思想：
 * - slow 指向已处理区域的末尾
 * - fast 遍历数组，找需要保留的元素
 *
 * 典型题目：
 * - 移动零
 * - 删除有序数组中的重复项
 * - 移除元素
 */

/**
 * 📌 3. 滑动窗口
 *
 * 适用条件：
 * - 连续子数组/子串问题
 * - 求最长或最短
 * - 满足/不满足某种条件
 *
 * ⚠️ 注意：如果有负数，滑动窗口可能不适用！
 *
 * 模板：
 * ```
 * let left = 0;
 * for (let right = 0; right < n; right++) {
 *   // 1. 扩张：加入 right 元素
 *
 *   // 2. 收缩：当不满足条件时
 *   while (需要收缩) {
 *     // 如果求最小，在这里更新结果
 *     // 移除 left 元素
 *     left++;
 *   }
 *
 *   // 如果求最大，在这里更新结果
 * }
 * ```
 *
 * 关键决策：
 * - 求最长 → 扩张时更新结果
 * - 求最短 → 收缩时更新结果
 *
 * 典型题目：
 * - 无重复字符的最长子串
 * - 长度最小的子数组
 * - 最小覆盖子串
 */

/**
 * 📌 4. 前缀和
 *
 * 适用条件：
 * - 频繁查询区间和
 * - 子数组和相关问题
 * - 可能有负数
 *
 * 模板：
 * ```
 * // 构建前缀和
 * const prefix = [0];
 * for (const num of nums) {
 *   prefix.push(prefix[prefix.length - 1] + num);
 * }
 *
 * // 查询区间 [i, j] 的和
 * const sum = prefix[j + 1] - prefix[i];
 * ```
 *
 * 进阶技巧：
 * - 前缀和 + 哈希表：找满足条件的区间个数
 * - 前缀积：区间乘积问题
 *
 * 典型题目：
 * - 和为K的子数组
 * - 除自身以外数组的乘积
 * - 区间和查询
 */

/**
 * 📌 5. 原地哈希
 *
 * 适用条件：
 * - 找缺失/重复的数字
 * - 数字范围在 [1, n]
 * - 要求 O(1) 空间
 *
 * 核心思想：
 * - 把数字 x 放到下标 x-1 的位置
 * - 或用正负号标记某个数是否出现
 *
 * 模板（交换法）：
 * ```
 * for (let i = 0; i < n; i++) {
 *   while (nums[i] >= 1 && nums[i] <= n && nums[nums[i] - 1] !== nums[i]) {
 *     swap(nums[i], nums[nums[i] - 1]);
 *   }
 * }
 * ```
 *
 * 典型题目：
 * - 缺失的第一个正数
 * - 找到所有数组中消失的数字
 * - 数组中重复的数据
 */

