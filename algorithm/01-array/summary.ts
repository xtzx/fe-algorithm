/**
 * 🧩 变形题识别技巧
 *
 * 1. 接雨水 = 对撞指针 + 预处理（或单调栈）
 *    本质：每个位置的水量 = min(左最高, 右最高) - 当前高度
 *
 * 2. 三数之和 = 排序 + 固定一个数 + 对撞指针
 *    本质：降维成两数之和
 *
 * 3. 最小覆盖子串 = 滑动窗口 + 哈希表计数
 *    本质：扩张直到满足，收缩找最优
 *
 * 4. 和为K的子数组 = 前缀和 + 哈希表
 *    本质：prefix[j] - prefix[i] = k → 找 prefix[i] = prefix[j] - k
 *
 * 5. 除自身以外的乘积 = 左右前缀积
 *    本质：result[i] = left[i] × right[i]
 */

