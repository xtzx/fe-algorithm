function buildPrefixSum(nums: number[]): number[] {
  const n = nums.length;
  const prefix: number[] = new Array(n + 1).fill(0);

  for (let i = 0; i < n; i++) {
    prefix[i + 1] = prefix[i] + nums[i];
  }

  return prefix;
}

