/**
 * 🧩 快慢指针系列
 *
 * ┌─────────────────────────────────────────────────────────────┐
 * │  找中点 → while (fast && fast.next) 返回靠右               │
 * │  找中点 → while (fast.next && fast.next.next) 返回靠左     │
 * │  检测环 → 快慢相遇即有环                                   │
 * │  找环入口 → 相遇后一个从头走，再次相遇是入口               │
 * │  倒数第K个 → 快指针先走K步                                 │
 * │  快乐数 → 数字也可以形成"链表"                             │
 * └─────────────────────────────────────────────────────────────┘
 *
 * 🧩 链表反转系列
 *
 * ┌─────────────────────────────────────────────────────────────┐
 * │  反转整个链表 → 迭代或递归                                  │
 * │  反转区间 [left, right] → 头插法或截取反转                  │
 * │  K 个一组反转 → 分组 + 反转 + 连接                         │
 * │  回文链表 → 找中点 + 反转后半 + 比较                       │
 * └─────────────────────────────────────────────────────────────┘
 *
 * 🧩 合并系列
 *
 * ┌─────────────────────────────────────────────────────────────┐
 * │  合并两个有序链表 → 双指针 + dummy                          │
 * │  合并K个有序链表 → 分治或优先队列                          │
 * │  排序链表 → 找中点 + 分治 + 合并                           │
 * │  重排链表 → 找中点 + 反转后半 + 交替合并                   │
 * └─────────────────────────────────────────────────────────────┘
 */

