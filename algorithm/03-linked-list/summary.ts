// 7. 常见错误总结
// ============================================================

/**
 * ⚠️ 链表常见错误：
 *
 * 1. 空指针访问
 *    错误: node.next.val  // node.next 可能为 null
 *    正确: node?.next?.val 或先检查
 *
 * 2. 链表断裂
 *    错误: curr.next = prev; curr = curr.next;
 *    正确: 先保存 next，再修改指针
 *
 * 3. 忘记断开连接
 *    错误: 分割链表时不断开原连接
 *    正确: mid.next = null;
 *
 * 4. 返回值错误
 *    错误: 返回 dummy 而不是 dummy.next
 *    正确: return dummy.next;
 *
 * 5. 循环条件错误
 *    错误: while (fast) // 快慢指针
 *    正确: while (fast && fast.next)
 */

