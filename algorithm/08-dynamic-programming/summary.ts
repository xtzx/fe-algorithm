/**
 * ┌─────────────────────────────────────────────────────────────┐
 * │                      易错点速查                              │
 * ├─────────────────────────────────────────────────────────────┤
 * │                                                             │
 * │ 【状态定义】                                                 │
 * │  ⚠️ "前 i 个" vs "第 i 个" vs "以 i 结尾"                  │
 * │  ⚠️ 状态含义要清晰，决定了转移方程和返回值                  │
 * │                                                             │
 * │ 【初始化】                                                   │
 * │  ⚠️ 最小值问题：初始化为 Infinity                           │
 * │  ⚠️ 最大值问题：初始化为 0 或 -Infinity                     │
 * │  ⚠️ 计数问题：dp[0] 通常为 1                               │
 * │  ⚠️ 存在性问题：dp[0] 通常为 true                          │
 * │                                                             │
 * │ 【遍历顺序】                                                 │
 * │  ⚠️ 0-1 背包：内层从后往前（避免重复选择）                  │
 * │  ⚠️ 完全背包：内层从前往后（允许重复选择）                  │
 * │  ⚠️ 确保转移时依赖的状态已经计算                            │
 * │                                                             │
 * │ 【边界处理】                                                 │
 * │  ⚠️ dp 数组大小是 n 还是 n+1？                              │
 * │  ⚠️ 下标访问是 i-1 还是 i？                                 │
 * │                                                             │
 * │ 【返回值】                                                   │
 * │  ⚠️ "以 i 结尾"的问题：返回 max(dp)                        │
 * │  ⚠️ "前 n 个"的问题：返回 dp[n]                            │
 * │  ⚠️ 无解情况：检查 Infinity 或 -1                           │
 * │                                                             │
 * │ 【空间优化】                                                 │
 * │  ⚠️ 优化后注意变量更新顺序                                  │
 * │  ⚠️ 需要用到 dp[i-1][j-1] 时要特别小心                     │
 * │                                                             │
 * └─────────────────────────────────────────────────────────────┘
 */

