// 6. 🧠 核心思维模型
// ============================================================

/**
 * 【DP 的本质】
 *
 * DP = 递归 + 记忆化 = 自底向上求解
 *
 * 核心思想：
 * 1. 大问题分解为小问题
 * 2. 保存小问题的解
 * 3. 从小问题推导大问题
 *
 * 【背包问题模型】
 *
 * 0-1 背包（每个物品只能选一次）：
 * ```
 * for (物品 i)
 *   for (容量 j 从大到小)  // 关键：从后往前
 *     dp[j] = max(dp[j], dp[j-w[i]] + v[i])
 * ```
 *
 * 完全背包（每个物品可以选无限次）：
 * ```
 * for (物品 i)
 *   for (容量 j 从小到大)  // 关键：从前往后
 *     dp[j] = max(dp[j], dp[j-w[i]] + v[i])
 * ```
 *
 * 【状态机模型】
 *
 * 买卖股票系列：
 * ```
 *        买入              卖出
 *        ─────►          ─────►
 *  [不持有] ◄───── [持有]
 *        ◄─────
 *         保持
 * ```
 */

