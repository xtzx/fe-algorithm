// 2. 📊 DP 五步法
// ============================================================

/**
 * 🔄 DP 解题步骤 (Mermaid):
 *
 * ```mermaid
 * flowchart TD
 *     A[Step 1: 定义状态] --> B[Step 2: 状态转移方程]
 *     B --> C[Step 3: 初始条件]
 *     C --> D[Step 4: 遍历顺序]
 *     D --> E[Step 5: 返回值]
 *
 *     A1[dp i 表示什么?] --> A
 *     B1[dp i 如何从之前状态推导?] --> B
 *     C1[dp 0 = ? 边界是什么?] --> C
 *     D1[从前往后? 从后往前?] --> D
 *     E1[返回 dp n? 还是 max dp?] --> E
 * ```
 *
 * 关键问题清单：
 * 1. 状态定义：dp[i] 具体表示什么？
 * 2. 转移方程：dp[i] 依赖哪些之前的状态？
 * 3. 初始化：最小子问题的答案是什么？
 * 4. 遍历顺序：如何保证依赖的状态已计算？
 * 5. 结果：最终答案在哪个位置？
 */

