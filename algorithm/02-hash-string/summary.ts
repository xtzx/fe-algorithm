/**
 * 📌 1. 哈希表查找配对（两数之和模式）
 *
 * 适用条件：
 * - 找两个数满足某种关系
 * - 需要 O(1) 查找
 *
 * 模板：
 * ```
 * const map = new Map();
 * for (let i = 0; i < nums.length; i++) {
 *   const complement = target - nums[i];
 *   if (map.has(complement)) {
 *     return [map.get(complement), i];
 *   }
 *   map.set(nums[i], i);
 * }
 * ```
 *
 * 关键：先查后存，避免找到自己
 */

/**
 * 📌 2. 哈希表计数统计
 *
 * 适用条件：
 * - 统计频率
 * - 判断是否满足频率条件
 *
 * 模板：
 * ```
 * const count = new Map();
 * for (const item of arr) {
 *   count.set(item, (count.get(item) || 0) + 1);
 * }
 * ```
 *
 * 变体：
 * - 两个哈希表比较
 * - 一个哈希表：+1 和 -1 抵消
 * - 数组代替 Map（固定字符集）
 */

/**
 * 📌 3. 滑动窗口 + 哈希表
 *
 * 适用条件：
 * - 连续子串/子数组
 * - 需要满足某种字符条件
 *
 * 模板：
 * ```
 * const window = new Map();
 * let left = 0, right = 0;
 * let valid = 0;
 *
 * while (right < s.length) {
 *   const c = s[right++];
 *   // 更新 window 和 valid
 *
 *   while (需要收缩) {
 *     const d = s[left++];
 *     // 更新 window 和 valid
 *   }
 * }
 * ```
 *
 * valid 的含义：满足条件的字符种类数
 */

/**
 * 📌 4. 异位词处理
 *
 * 判断两个词是异位词：
 * - 方法1：排序后比较
 * - 方法2：字符计数比较
 *
 * 异位词分组：
 * - 排序后的字符串作为 key
 * - 字符计数的字符串表示作为 key
 *
 * 找所有异位词：
 * - 滑动窗口 + 字符计数
 * - 窗口大小固定为目标长度
 */

/**
 * 📌 5. 回文处理
 *
 * 判断回文：
 * - 双指针从两端向中间
 *
 * 最长回文子串：
 * - 中心扩展法（推荐）
 * - 动态规划
 *
 * 回文相关的优化：
 * - 从中心向外扩展
 * - 预处理（回文判断表）
 */

/**
 * 📌 6. 大数运算
 *
 * 适用条件：
 * - 数字超出 Number 范围
 * - 不能直接转换为整数
 *
 * 模板：
 * - 从右往左逐位计算
 * - 处理进位
 * - 结果可能需要反转
 */

