/**
 * 🧩 两数之和系列
 *
 * ┌─────────────────────────────────────────────────────────────┐
 * │  两数之和（无序）→ 哈希表 O(n)                              │
 * │  两数之和（有序）→ 双指针 O(n)                              │
 * │  三数之和 → 排序 + 固定一个 + 双指针 O(n²)                  │
 * │  四数相加 II → 两两分组 + 哈希表 O(n²)                      │
 * └─────────────────────────────────────────────────────────────┘
 *
 * 🧩 异位词系列
 *
 * ┌─────────────────────────────────────────────────────────────┐
 * │  有效异位词 → 排序比较 / 字符计数                           │
 * │  异位词分组 → 排序后字符串作为 key                          │
 * │  找所有异位词 → 滑动窗口 + 字符计数                         │
 * └─────────────────────────────────────────────────────────────┘
 *
 * 🧩 回文系列
 *
 * ┌─────────────────────────────────────────────────────────────┐
 * │  判断回文 → 双指针                                          │
 * │  最长回文子串 → 中心扩展 / DP / Manacher                    │
 * │  回文子串个数 → 中心扩展                                    │
 * │  分割回文串 → 回溯 + 预处理                                 │
 * │  回文对 → 哈希表 + 前后缀枚举                               │
 * └─────────────────────────────────────────────────────────────┘
 *
 * 🧩 大数运算系列
 *
 * ┌─────────────────────────────────────────────────────────────┐
 * │  字符串相加 → 逐位相加 + 进位                               │
 * │  字符串相乘 → 模拟竖式 result[i+j] += a[i] * b[j]          │
 * │  二进制加法 → 进位是 /2，取模是 %2                          │
 * │  36进制加法 → 字节高频面试题                                │
 * └─────────────────────────────────────────────────────────────┘
 */

