// 2. 沟通技巧
// ============================================================

/**
 * 【边思考边说话】
 *
 * 面试不是默写代码，要让面试官了解你的思考过程：
 *
 * ✅ 好的表达：
 * "这道题让我想到了滑动窗口，因为要找连续子数组..."
 * "我先想一下暴力解法，遍历所有可能的子数组，复杂度是 O(n²)..."
 * "我觉得可以用哈希表优化，把查找从 O(n) 降到 O(1)..."
 *
 * ❌ 差的表达：
 * （沉默 5 分钟后）"我想好了，开始写代码。"
 * （只顾写代码，不解释）
 *
 * 【主动提问】
 *
 * 好的问题示例：
 * • "输入可以是空数组吗？"
 * • "有重复元素的话怎么处理？"
 * • "如果有多个解，返回任意一个还是有优先级？"
 * • "数据规模大概是多少？这会影响我选择的算法。"
 *
 * 【确认理解】
 *
 * 在开始写代码前：
 * "我复述一下我的理解：给定一个数组，找到...，返回...，对吗？"
 *
 * 在想好解法后：
 * "我打算用 XX 方法，时间复杂度是 O(n)，空间复杂度是 O(n)，可以开始写吗？"
 */

/**
 * 【标准沟通模板】
 *
 * ┌─────────────────────────────────────────────────────────────┐
 * │                      面试沟通模板                            │
 * ├─────────────────────────────────────────────────────────────┤
 * │                                                             │
 * │  【理解题意时】                                               │
 * │  "让我确认一下理解是否正确：..."                              │
 * │  "这道题的输入是...，输出是...，对吗？"                       │
 * │  "请问数据规模大概是多少？"                                   │
 * │                                                             │
 * │  【思考解法时】                                               │
 * │  "我先想一个暴力解法..."                                     │
 * │  "暴力解法的复杂度是 O(n²)，可能会超时..."                   │
 * │  "我觉得可以用 XX 优化，因为..."                             │
 * │                                                             │
 * │  【编写代码时】                                               │
 * │  "我先写一个框架..."                                         │
 * │  "这里我用双指针是因为..."                                   │
 * │  "这个边界条件需要特殊处理..."                               │
 * │                                                             │
 * │  【测试代码时】                                               │
 * │  "让我用这个例子走一遍..."                                   │
 * │  "这里有个小问题，让我修复一下..."                           │
 * │                                                             │
 * │  【总结时】                                                   │
 * │  "时间复杂度是 O(n)，空间复杂度是 O(1)..."                   │
 * │  "如果要进一步优化的话，可以考虑..."                         │
 * │                                                             │
 * └─────────────────────────────────────────────────────────────┘
 */

