/**
 * 【白板/纸笔面试技巧】
 *
 * ┌─────────────────────────────────────────────────────────────┐
 * │                                                             │
 * │  1. 空间预留                                                 │
 * │     • 每行之间留空，方便后续插入修改                         │
 * │     • 函数开头留几行，可能需要加初始化代码                   │
 * │                                                             │
 * │  2. 缩进对齐                                                 │
 * │     • 提前在左边画辅助线                                     │
 * │     • 保持 2 或 4 个空格的统一缩进                           │
 * │                                                             │
 * │  3. 先写框架                                                 │
 * │     function solve(input) {                                 │
 * │       // 边界处理                                           │
 * │                                                             │
 * │       // 初始化                                             │
 * │                                                             │
 * │       // 主逻辑                                             │
 * │                                                             │
 * │       // 返回结果                                           │
 * │     }                                                       │
 * │                                                             │
 * │  4. 用伪代码注释                                             │
 * │     // for each element                                    │
 * │     //   if condition met                                  │
 * │     //     update result                                   │
 * │                                                             │
 * │  5. 修改方式                                                 │
 * │     • 小修改：划掉，在旁边写                                 │
 * │     • 大修改：说"这里需要改一下"，另起一处写                 │
 * │                                                             │
 * └─────────────────────────────────────────────────────────────┘
 */

/**
 * 【在线编程技巧】
 *
 * 1. 善用 console.log 调试
 * 2. 写完后直接运行测试
 * 3. 先用简单用例，再用复杂用例
 * 4. 遇到报错先读错误信息
 */

