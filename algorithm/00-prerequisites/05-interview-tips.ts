/**
 * 【Q: 面试官不说话/面无表情怎么办？】
 * A: 这是正常的，继续你的表达。很多面试官习惯安静观察。
 *
 * 【Q: 写完代码发现有 bug 怎么办？】
 * A: "让我检查一下...这里有个问题，我来修复。"
 *    冷静修改，不要慌张。发现并修复 bug 是加分项。
 *
 * 【Q: 面试官打断我怎么办？】
 * A: 仔细听，可能是在给提示。回应："好的，您的意思是...？"
 *
 * 【Q: 题目太难了怎么办？】
 * A: 说出暴力解法，请求提示，展示思考过程。
 *    面试官也会根据题目难度调整评价标准。
 *
 * 【Q: 遇到没见过的题怎么办？】
 * A: 用信号词分析，尝试归约到熟悉的模型。
 *    说："这道题让我想到了 XX，可能可以用 YY 方法。"
 *
 * 【Q: 时间快到了但代码还没写完怎么办？】
 * A: 先说清楚剩余的逻辑："因为时间关系，我说一下剩余部分..."
 *    把核心思路表达清楚比写完代码更重要。
 */

