// 5. 答不出来时怎么办
// ============================================================

/**
 * 【策略一：说出你知道的】
 *
 * "这道题我暂时没想到最优解，但我可以说说我的思考过程..."
 * "我知道这可能和 XX 问题类似..."
 * "暴力解法我可以写出来..."
 *
 * 【策略二：暴力解法兜底】
 *
 * 至少写出一个能工作的解法，展示基本编码能力：
 * "我先写一个暴力解法，然后我们可以讨论优化方向。"
 *
 * 【策略三：请求提示】
 *
 * 适当时候可以请求提示（不要太频繁）：
 * "我卡在 XX 这里了，能给点提示吗？"
 * "这道题是不是可以用 XX 方法？"
 *
 * 【策略四：换个思路】
 *
 * 如果一个方向行不通，换一个：
 * "双指针好像行不通，让我试试哈希表..."
 * "贪心不太对，这可能需要 DP..."
 *
 * 【心态调整】
 *
 * • 答不出来很正常，面试官知道这是压力环境
 * • 展示思考过程比给出答案更重要
 * • 态度积极、愿意尝试会加分
 */

