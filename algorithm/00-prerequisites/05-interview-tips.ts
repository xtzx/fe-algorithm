// 3. 常见陷阱
// ============================================================

/**
 * 【陷阱一：没看清题目】
 *
 * 常见错误：
 * • 返回下标 vs 返回值
 * • 子数组 vs 子序列
 * • 严格递增 vs 非严格递增
 * • 有序 vs 无序
 *
 * 预防：认真读题，复述确认
 *
 * 【陷阱二：边界情况遗漏】
 *
 * 常见遗漏：
 * • 空数组/空字符串
 * • 只有一个元素
 * • 所有元素相同
 * • 负数/零
 *
 * 预防：写完后主动用边界用例测试
 *
 * 【陷阱三：复杂度估算错误】
 *
 * 常见错误：
 * • "这是 O(n)" （实际是 O(n²)）
 * • 忽略了嵌套循环
 * • 忽略了递归调用
 *
 * 预防：写之前和写之后都分析一下
 *
 * 【陷阱四：代码风格差】
 *
 * 常见问题：
 * • 变量名 a, b, c
 * • 没有缩进
 * • 超长函数没有拆分
 *
 * 预防：按照规范来，面试官也会看代码质量
 *
 * 【陷阱五：钻牛角尖】
 *
 * 常见情况：
 * • 一种方法卡住，不尝试其他方法
 * • 追求完美解法，忽略了时间限制
 *
 * 预防：卡住 5 分钟就换思路，先保证有一个能跑的解法
 */

