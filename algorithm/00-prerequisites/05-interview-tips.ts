/**
 * 【标准 45 分钟算法面试】
 *
 * ┌─────────────────────────────────────────────────────────────┐
 * │                      时间分配建议                            │
 * ├─────────────────────────────────────────────────────────────┤
 * │                                                             │
 * │   0-5 分钟    理解题意                                       │
 * │   ──────────────────────────────────                        │
 * │   • 复述题目，确认理解正确                                   │
 * │   • 问清楚边界情况和约束条件                                 │
 * │   • 看数据规模，估计复杂度要求                               │
 * │                                                             │
 * │   5-10 分钟   思考解法                                       │
 * │   ──────────────────────────────────                        │
 * │   • 说出暴力解法（展示基本能力）                             │
 * │   • 寻找优化方向                                             │
 * │   • 确定最终方案，和面试官确认                               │
 * │                                                             │
 * │   10-30 分钟  编写代码                                       │
 * │   ──────────────────────────────────                        │
 * │   • 边写边解释思路                                           │
 * │   • 先写框架，再填细节                                       │
 * │   • 注意代码风格                                             │
 * │                                                             │
 * │   30-40 分钟  测试调试                                       │
 * │   ──────────────────────────────────                        │
 * │   • 用例子走一遍代码                                         │
 * │   • 检查边界情况                                             │
 * │   • 修复发现的 bug                                           │
 * │                                                             │
 * │   40-45 分钟  复杂度分析 + 优化讨论                          │
 * │   ──────────────────────────────────                        │
 * │   • 说明时间/空间复杂度                                      │
 * │   • 提及其他可能的解法                                       │
 * │   • 讨论 trade-off                                          │
 * │                                                             │
 * └─────────────────────────────────────────────────────────────┘
 *
 * 【按难度调整时间】
 *
 * Easy 题：思考 3 分钟，编码 10 分钟
 * Medium 题：思考 5-8 分钟，编码 15-20 分钟
 * Hard 题：思考 10 分钟，编码 20-25 分钟
 */

