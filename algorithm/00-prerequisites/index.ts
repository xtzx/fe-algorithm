/**
 * 【模块一：复杂度分析】01-complexity.ts
 *
 * ┌─────────────────────────────────────────────────────────────┐
 * │ • 大 O 表示法：定义、常见复杂度                              │
 * │ • 复杂度对比：O(1) < O(log n) < O(n) < O(n²) < O(2^n)       │
 * │ • 分析技巧：循环、递归、均摊分析                             │
 * │ • 数据规模：10^6 → O(n log n)，10^3 → O(n²)                │
 * │ • 空间复杂度：栈空间、辅助空间                               │
 * └─────────────────────────────────────────────────────────────┘
 *
 * 【模块二：基础数学】02-math-basics.ts
 *
 * ┌─────────────────────────────────────────────────────────────┐
 * │ • 对数与指数：log₂n、2^n 的性质                              │
 * │ • 排列组合：P(n,k)、C(n,k)、全排列数                         │
 * │ • 等差等比数列：求和公式                                     │
 * │ • 取模运算：(a+b)%m、(a×b)%m                                │
 * │ • 位运算：&、|、^、<<、>>、n&(n-1)                          │
 * └─────────────────────────────────────────────────────────────┘
 *
 * 【模块三：问题建模思维】03-problem-modeling.ts ⭐ 核心
 *
 * ┌─────────────────────────────────────────────────────────────┐
 * │ • 问题识别四问：输入？输出？约束？规模？                      │
 * │ • 信号词速查表：关键词 → 算法映射                            │
 * │ • 问题归约思维：新题 → 变形 → 熟悉模型                       │
 * │ • 变形题识别：常见变形模式                                   │
 * │ • 算法选择决策树：根据特征选算法                             │
 * └─────────────────────────────────────────────────────────────┘
 *
 * 【模块四：解题方法论】04-methodology.ts
 *
 * ┌─────────────────────────────────────────────────────────────┐
 * │ • 通用解题框架：理解 → 暴力 → 优化 → 实现 → 测试             │
 * │ • 优化思路路径：枚举 → 排序 → 哈希 → 双指针 → DP            │
 * │ • 调试技巧：打印、画图、边界测试                             │
 * │ • 边界情况清单：空、单元素、全相同、极值、负数               │
 * └─────────────────────────────────────────────────────────────┘
 *
 * 【模块五：面试技巧】05-interview-tips.ts
 *
 * ┌─────────────────────────────────────────────────────────────┐
 * │ • 时间分配：5分钟理解 + 5分钟思路 + 15分钟编码 + 5分钟测试   │
 * │ • 沟通技巧：复述题意、边想边说、主动提问                     │
 * │ • 常见陷阱：没看清题、边界遗漏、复杂度算错                   │
 * │ • 手写代码：先写框架、空行预留、缩进对齐                     │
 * │ • 答不出来：说出已知、暴力兜底、请求提示                     │
 * └─────────────────────────────────────────────────────────────┘
 *
 * 【模块六：前端场景关联】06-frontend-scenarios.ts
 *
 * ┌─────────────────────────────────────────────────────────────┐
 * │ • 虚拟列表：二分查找定位可视区域                             │
 * │ • DOM Diff：最长公共子序列 / 最长递增子序列                  │
 * │ • 权限树：DFS/BFS 遍历                                      │
 * │ • 组件依赖：拓扑排序检测循环依赖                             │
 * │ • 防抖节流：滑动窗口思想                                     │
 * │ • LRU 缓存：哈希表 + 链表（Map 实现）                        │
 * └─────────────────────────────────────────────────────────────┘
 */

