/**
 * 看到新题时的思考路径：
 *
 * Q: 这是回溯问题吗？
 * A: 检查以下特征：
 *    1. 需要穷举所有可能？
 *    2. 结果是一个集合/列表？
 *    3. 每个结果是一系列选择的组合？
 *
 * Q: 是哪种类型？
 * A: 看结果的性质：
 *    • [1,2] 和 [2,1] 一样吗？
 *      一样 → 组合/子集
 *      不一样 → 排列
 *
 * Q: 需要去重吗？
 * A: 看是否有重复元素
 *    有 → 排序 + 相应去重逻辑
 *    无 → 不需要
 *
 * Q: 可以剪枝吗？
 * A: 看是否有约束条件
 *    有 → 不满足条件时提前返回
 *    无 → 完整遍历
 *
 * 【关键问句】
 *
 * 1. "这个问题需要找出所有可能的XX吗？"
 *    → 需要 → 回溯
 *
 * 2. "顺序重要吗？"
 *    → 重要 → 排列（used）
 *    → 不重要 → 组合/子集（start）
 *
 * 3. "有目标条件吗？"
 *    → 有 → 组合
 *    → 无 → 子集
 *
 * 4. "有约束条件吗？"
 *    → 有 → 考虑剪枝
 */

