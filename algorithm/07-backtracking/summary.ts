// 7. ⚠️ 易错点速查
// ============================================================

/**
 * ┌─────────────────────────────────────────────────────────────┐
 * │                      易错点速查                              │
 * ├─────────────────────────────────────────────────────────────┤
 * │                                                             │
 * │ 【结果收集】                                                 │
 * │  ⚠️ result.push([...path])，必须拷贝！                      │
 * │  ⚠️ 不能 result.push(path)                                 │
 * │                                                             │
 * │ 【撤销选择】                                                 │
 * │  ⚠️ 做选择和撤销必须配对                                    │
 * │  ⚠️ path.push() ↔ path.pop()                               │
 * │  ⚠️ used[i] = true ↔ used[i] = false                       │
 * │                                                             │
 * │ 【去重方式】                                                 │
 * │  ⚠️ 排列用 used，组合/子集用 start                          │
 * │  ⚠️ 有重复元素时必须先排序                                  │
 * │                                                             │
 * │ 【组合去重条件】                                             │
 * │  ⚠️ i > start（同一层）                                     │
 * │  ⚠️ 不是 i > 0                                             │
 * │                                                             │
 * │ 【排列去重条件】                                             │
 * │  ⚠️ !used[i-1]（前一个没被使用=同一层）                     │
 * │  ⚠️ 不是 used[i-1]                                         │
 * │                                                             │
 * │ 【可重复使用】                                               │
 * │  ⚠️ 可重复：backtrack(i, ...)                               │
 * │  ⚠️ 不可重复：backtrack(i+1, ...)                           │
 * │                                                             │
 * │ 【剪枝优化】                                                 │
 * │  ⚠️ 排序后可以用 break 提前退出                             │
 * │  ⚠️ 不满足条件用 continue 跳过                              │
 * │                                                             │
 * └─────────────────────────────────────────────────────────────┘
 */

