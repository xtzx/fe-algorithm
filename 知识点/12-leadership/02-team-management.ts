/**
 * 📊 绩效管理
 *
 * OKR（目标与关键结果）
 * - O：目标，定性，鼓舞人心
 * - KR：关键结果，定量，可衡量
 *
 * 绩效沟通
 * - 目标对齐（开始）
 * - 过程反馈（持续）
 * - 绩效评估（周期）
 */

const okrExample = `
# Q1 团队 OKR

## O1：提升前端性能体验
### KR
- KR1：首屏 LCP P95 < 2.5s（当前 3.2s）
- KR2：核心页面 TTI < 3s（当前 4.1s）
- KR3：页面崩溃率 < 0.1%（当前 0.3%）

## O2：建立质量保障体系
### KR
- KR1：单元测试覆盖率 > 80%（当前 50%）
- KR2：关键流程 E2E 覆盖 100%
- KR3：线上 Bug 率下降 50%

## O3：提升团队技术能力
### KR
- KR1：每人至少一次技术分享
- KR2：完成 2 个技术专项
- KR3：培养 1 名高级工程师

## 个人 OKR 示例

### O：成为性能优化专家
- KR1：完成 3 个性能优化项目
- KR2：输出性能优化最佳实践文档
- KR3：完成一次全团队性能培训
`;

