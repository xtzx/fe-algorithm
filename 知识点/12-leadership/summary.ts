/**
 * 行为题 1：介绍一次团队协作经历
 *
 * 回答要点：
 * - 背景：什么项目，什么团队
 * - 我的角色：负责什么
 * - 协作过程：如何配合
 * - 困难与解决：遇到什么问题
 * - 结果：最终效果
 */

/**
 * 行为题 2：介绍一次解决复杂问题的经历
 *
 * 回答要点：
 * - 问题描述：什么问题，多复杂
 * - 分析过程：如何定位
 * - 解决方案：怎么解决
 * - 结果与反思：效果和经验
 */

/**
 * 行为题 3：你的优势和不足是什么？
 *
 * 回答要点：
 * - 优势：结合岗位要求，举例说明
 * - 不足：真实但不致命，同时说明改进计划
 *
 * ⚠️ 易错点：
 * - 不足说成优势（我太追求完美了）
 * - 不足与岗位冲突
 */

/**
 * 行为题 4：为什么离职/为什么选择我们？
 *
 * 离职原因：
 * - 发展空间
 * - 技术方向
 * - 个人规划
 * （避免抱怨前公司）
 *
 * 选择原因：
 * - 业务认可
 * - 技术挑战
 * - 团队氛围
 * - 发展空间
 */

