/**
 * 1. 你的职业规划是什么？
 *
 * 回答框架：
 * - 短期（1-2年）：技术深度，成为领域专家
 * - 中期（3-5年）：技术广度 + 带人能力
 * - 长期：技术管理 or 技术专家
 *
 * ⚠️ 易错点：
 * - 太空泛（我想当 CTO）
 * - 与岗位不匹配
 * - 没有具体规划
 *
 * 💡 追问：为什么选择这个方向？
 * - 结合自身优势
 * - 结合行业趋势
 * - 结合公司发展
 */

/**
 * 2. 介绍一个你主导的技术项目
 *
 * STAR 法则：
 * - Situation：项目背景
 * - Task：我的角色和任务
 * - Action：具体做了什么
 * - Result：取得什么结果
 *
 * ⚠️ 易错点：
 * - 只说做了什么，没说为什么
 * - 没有量化结果
 * - 没有体现个人价值
 *
 * 💡 追问：
 * - 遇到什么困难？怎么解决的？
 * - 如果重新做会怎么改进？
 * - 有什么经验教训？
 */

/**
 * 3. 如何进行技术选型？
 *
 * 决策框架：
 * 1. 明确需求和约束
 * 2. 调研候选方案
 * 3. 多维度评估（功能、性能、成本、团队）
 * 4. 记录决策过程
 *
 * ⚠️ 易错点：
 * - 只看技术不看业务
 * - 忽略团队因素
 * - 没有 B 方案
 *
 * 💡 追问：举一个技术选型的例子？
 */

/**
 * 4. 如何带团队？
 *
 * 核心能力：
 * - 目标设定：OKR 对齐
 * - 人才培养：梯队建设
 * - 绩效管理：过程反馈
 * - 文化建设：技术氛围
 *
 * ⚠️ 易错点：
 * - 只管事不管人
 * - 没有培养意识
 * - 缺少沟通
 *
 * 💡 追问：如何培养新人？
 * - 导师制
 * - 学习路径
 * - 定期反馈
 */

// ============================================================
