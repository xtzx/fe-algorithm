/**
 * 5. 大数据量图表如何优化？
 *
 * 优化策略：
 * - 数据采样（LTTB 算法）
 * - 分层渲染（静态/动态分离）
 * - 离屏渲染
 * - 增量渲染
 * - 虚拟化
 * - WebGL
 *
 * ⚠️ 易错点：
 * - 采样要保留极值点
 *
 * 💡 追问：什么是 LTTB 采样？
 * - Largest-Triangle-Three-Buckets
 * - 保持曲线形状的降采样
 */

/**
 * 6. 富文本编辑器如何设计？
 *
 * 核心模块：
 * - 数据模型（嵌套树或扁平序列）
 * - 选区管理
 * - 操作系统（Transform）
 * - 撤销/重做
 * - 插件系统
 *
 * ⚠️ 易错点：
 * - 中文输入需要处理 composition 事件
 *
 * 💡 追问：OT 和 CRDT 的区别？
 * - OT：需要服务端协调
 * - CRDT：数据结构保证最终一致
 */

/**
 * 7. 小程序 setData 为什么慢？如何优化？
 *
 * 原因：
 * - 跨线程通信（渲染层 ↔ 逻辑层）
 * - JSON 序列化
 * - 数据量大开销大
 *
 * 优化：
 * - 减少调用次数
 * - 只更新必要数据
 * - 使用路径更新
 * - 避免大数据传输
 *
 * ⚠️ 易错点：
 * - 循环中 setData 性能极差
 *
 * 💡 追问：为什么小程序用双线程？
 * - 安全：JS 无法直接操作 DOM
 * - 稳定：JS 卡死不影响渲染
 */

/**
 * 8. Node.js 如何处理 CPU 密集型任务？
 *
 * 方案：
 * - worker_threads（推荐）
 * - child_process.fork
 * - 任务队列（Bull/BullMQ）
 * - 外部服务（Go/Rust）
 *
 * ⚠️ 易错点：
 * - cluster 是多进程，不是多线程
 *
 * 💡 追问：worker_threads 和 cluster 的区别？
 * - worker_threads：共享内存，适合 CPU 密集
 * - cluster：独立进程，适合 HTTP 服务扩展
 */

