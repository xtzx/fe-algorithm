/**
 * 1. SVG、Canvas、WebGL 如何选择？
 *
 * SVG：
 * - 矢量图，DOM 元素
 * - 适合元素少、需要交互
 * - < 1000 个元素
 *
 * Canvas：
 * - 位图，像素操作
 * - 适合动画、图表
 * - < 10000 个元素
 *
 * WebGL：
 * - GPU 加速
 * - 适合 3D、大数据量
 * - 百万级元素
 *
 * ⚠️ 易错点：
 * - Canvas 高清屏需要处理 devicePixelRatio
 *
 * 💡 追问：Canvas 如何实现事件交互？
 * - 监听 canvas 事件
 * - 根据坐标判断点击的元素
 */

/**
 * 2. 低代码平台的核心模块有哪些？
 *
 * 核心模块：
 * - 设计器（拖拽、配置）
 * - 渲染器（Schema → 页面）
 * - 物料系统（组件库）
 * - 出码引擎（Schema → 代码）
 * - 数据源管理
 * - 逻辑编排
 *
 * ⚠️ 易错点：
 * - 表达式执行需要沙箱隔离
 *
 * 💡 追问：如何设计 Schema？
 * - 组件树结构
 * - props 支持表达式
 * - 事件、循环、条件
 */

/**
 * 3. Node.js 事件循环和浏览器的区别？
 *
 * 浏览器：
 * - 宏任务 → 微任务 → 渲染 → 宏任务
 *
 * Node.js：
 * - 6 个阶段：timers → pending → idle → poll → check → close
 * - 每个阶段之间执行 nextTick 和微任务
 * - setImmediate 是 Node 特有
 *
 * ⚠️ 易错点：
 * - setTimeout(0) 和 setImmediate 顺序不确定
 * - I/O 回调中 setImmediate 先执行
 *
 * 💡 追问：process.nextTick 和 Promise 哪个先？
 * - nextTick 优先级更高
 */

/**
 * 4. React Native 和 Flutter 如何选择？
 *
 * React Native：
 * - JavaScript，团队容易上手
 * - 支持热更新
 * - 原生组件渲染
 * - 性能中等
 *
 * Flutter：
 * - Dart 语言，学习成本
 * - 热更新受限
 * - 自绘引擎（Skia）
 * - 性能更好
 *
 * ⚠️ 易错点：
 * - RN 新架构（JSI/Fabric）性能提升
 *
 * 💡 追问：RN 的 Bridge 有什么问题？
 * - 异步通信
 * - JSON 序列化开销
 * - 无法同步调用
 */

