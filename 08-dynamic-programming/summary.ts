/**
 * 看到新题时的思考路径：
 *
 * Q: 这是 DP 问题吗？
 * A: 检查以下特征：
 *    1. 求最优解（最大/最小/最长/最短）？
 *    2. 求方案数？
 *    3. 求可行性（是否可能）？
 *    4. 有重叠子问题？（同一子问题被多次计算）
 *    5. 有最优子结构？（大问题的最优解包含小问题的最优解）
 *
 * Q: 是哪种 DP？
 * A: 看状态维度：
 *    • 一维线性：爬楼梯、打家劫舍
 *    • 二维网格：不同路径、最小路径和
 *    • 二维序列：LCS、编辑距离
 *    • 背包变体：选或不选、容量限制
 *    • 区间 DP：区间合并、戳气球
 *    • 状态机：股票买卖
 *
 * 【关键问句】
 *
 * 1. "状态怎么定义？"
 *    → dp[i] 具体表示什么
 *
 * 2. "当前状态依赖哪些之前的状态？"
 *    → 状态转移方程
 *
 * 3. "最小的子问题是什么？答案是多少？"
 *    → 初始化条件
 *
 * 4. "如何遍历保证依赖已计算？"
 *    → 遍历顺序
 */

