// 🔄 解法对比
// ============================================================
/**
 * | 解法       | 时间  | 空间  | 特点                     |
 * |-----------|-------|-------|-------------------------|
 * | 双指针    | O(n)  | O(1)  | 推荐，空间最优            |
 * | 动态规划  | O(n)  | O(n)  | 思路清晰，易理解          |
 * | 单调栈    | O(n)  | O(n)  | 另一种视角，横向计算       |
 */

// ============================================================
// ⚠️ 易错点
// ============================================================
/**
 * 1. 双指针移动条件：
 *    - 移动较矮的一端
 *    - 相等时移动哪边都可以
 *
 * 2. 雨水计算：
 *    - min(leftMax, rightMax) - height[i]
 *    - 不是 max！
 *
 * 3. 边界情况：
 *    - 长度 <= 2 无法接水
 *    - 单调递增或递减也无法接水
 *
 * 4. 单调栈的计算方式：
 *    - 计算的是水平层的雨水
 *    - 需要有左右边界才能接水
 */

