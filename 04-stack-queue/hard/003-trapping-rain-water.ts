/**
 * 📝 题目：接雨水
 * 🔗 链接：https://leetcode.cn/problems/trapping-rain-water/
 * 🏷️ 难度：Hard
 * 🏷️ 标签：栈、数组、双指针、动态规划、单调栈
 *
 * 📋 题目描述：
 * 给定 n 个非负整数表示每个宽度为 1 的柱子的高度图，
 * 计算按此排列的柱子，下雨之后能接多少雨水。
 *
 * 示例：
 * 输入：height = [0,1,0,2,1,0,1,3,2,1,2,1]
 * 输出：6
 *
 *           █
 *       █···██·█
 *   █···██·█████
 * ─────────────────
 *
 * · 表示接的雨水，共 6 个单位
 */

// ============================================================
// 💡 思路分析：这道题的解法是怎么想出来的？
// ============================================================
//
// 核心思想：
// 每个位置能接的雨水 = min(左边最高, 右边最高) - 当前高度
//
// 方法一：动态规划
// - 预处理每个位置左边和右边的最大高度
// - 时间 O(n)，空间 O(n)
//
// 方法二：双指针
// - 从两端向中间移动
// - 移动较矮的一端
// - 时间 O(n)，空间 O(1)
//
// 方法三：单调栈
// - 维护一个单调递减栈
// - 遇到更高的柱子时，计算能接的雨水
// - 时间 O(n)，空间 O(n)

// ============================================================
// 解法一：双指针（推荐，空间最优）
// ============================================================
// ⏱️ 时间复杂度：O(n) | 空间复杂度：O(1)

/**
 * 📊 双指针思路图解：
 *
 * 关键洞察：
 * - 如果左边最高 < 右边最高，那么当前位置能接的雨水取决于左边
 * - 反之取决于右边
 * - 所以可以用双指针，移动较矮的一端
 *
 * height = [0, 1, 0, 2, 1, 0, 1, 3, 2, 1, 2, 1]
 *           L                             R
 *
 * leftMax = 0, rightMax = 1
 * height[L]=0 <= height[R]=1
 * 左边更矮，处理左边
 * water += max(0, leftMax - height[L]) = 0
 * leftMax = max(0, 0) = 0
 * L++
 *
 * ... 逐步移动指针，累加雨水
 */
function trap(height: number[]): number {
  if (height.length <= 2) return 0;

  let left = 0;
  let right = height.length - 1;
  let leftMax = 0;
  let rightMax = 0;
  let water = 0;

  while (left < right) {
    if (height[left] < height[right]) {
      // 左边更矮，处理左边
      if (height[left] >= leftMax) {
        leftMax = height[left];
      } else {
        water += leftMax - height[left];
      }
      left++;
    } else {
      // 右边更矮（或相等），处理右边
      if (height[right] >= rightMax) {
        rightMax = height[right];
      } else {
        water += rightMax - height[right];
      }
      right--;
    }
  }

  return water;
}

// ============================================================
// 解法二：动态规划（预处理）
// ============================================================
// ⏱️ 时间复杂度：O(n) | 空间复杂度：O(n)

/**
 * 📊 动态规划思路：
 *
 * 预处理两个数组：
 * leftMax[i] = max(height[0..i])  // i 左边（含）的最大高度
 * rightMax[i] = max(height[i..n-1])  // i 右边（含）的最大高度
 *
 * 每个位置的雨水 = min(leftMax[i], rightMax[i]) - height[i]
 */
function trap_v2(height: number[]): number {
  const n = height.length;
  if (n <= 2) return 0;

  // 预处理左边最大高度
  const leftMax = new Array(n);
  leftMax[0] = height[0];
  for (let i = 1; i < n; i++) {
    leftMax[i] = Math.max(leftMax[i - 1], height[i]);
  }

  // 预处理右边最大高度
  const rightMax = new Array(n);
  rightMax[n - 1] = height[n - 1];
  for (let i = n - 2; i >= 0; i--) {
    rightMax[i] = Math.max(rightMax[i + 1], height[i]);
  }

  // 计算雨水
  let water = 0;
  for (let i = 0; i < n; i++) {
    water += Math.min(leftMax[i], rightMax[i]) - height[i];
  }

  return water;
}

// ============================================================
// 解法三：单调栈
// ============================================================
// ⏱️ 时间复杂度：O(n) | 空间复杂度：O(n)

/**
 * 📊 单调栈思路：
 *
 * 维护单调递减栈
 * 当遇到更高的柱子时，说明形成了一个凹槽
 * 可以计算凹槽能接的雨水
 *
 * 计算的是「水平方向」的雨水层
 */
function trap_v3(height: number[]): number {
  const stack: number[] = [];
  let water = 0;

  for (let i = 0; i < height.length; i++) {
    // 当前柱子比栈顶高，形成凹槽
    while (stack.length > 0 && height[stack[stack.length - 1]] < height[i]) {
      const bottom = stack.pop()!; // 凹槽底部

      if (stack.length === 0) break; // 左边没有柱子，无法接水

      const left = stack[stack.length - 1]; // 凹槽左边界
      const h = Math.min(height[left], height[i]) - height[bottom];
      const w = i - left - 1;
      water += h * w;
    }
    stack.push(i);
  }

  return water;
}

