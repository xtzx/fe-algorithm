#!/usr/bin/env python3
"""
01_hello.py - Python 入门示例

演示：
- print() 函数
- 注释
- 基本语法
"""

# =============================================================================
# 1. Hello World
# =============================================================================

print("Hello, Python!")
print('单引号也可以')

# =============================================================================
# 2. print() 的参数
# =============================================================================

# sep：分隔符（默认空格）
print("Python", "is", "awesome")        # Python is awesome
print("Python", "is", "awesome", sep="-")  # Python-is-awesome

# end：结尾字符（默认换行）
print("Hello", end=" ")
print("World")  # 同一行：Hello World

# =============================================================================
# 3. 多行字符串
# =============================================================================

message = """
这是一个
多行
字符串
"""
print(message)

# =============================================================================
# 4. 注释
# =============================================================================

# 这是单行注释

"""
这是多行注释
（实际上是字符串，但常用于注释）
"""

# =============================================================================
# 5. 与 JavaScript 对比
# =============================================================================

# Python                     | JavaScript
# ---------------------------|---------------------------
# print("Hello")             | console.log("Hello")
# # 单行注释                  | // 单行注释
# """多行"""                  | /* 多行 */
# 无需分号                    | 可选分号
# 缩进即代码块                | {} 代码块

print("\n=== 运行完成 ===")

