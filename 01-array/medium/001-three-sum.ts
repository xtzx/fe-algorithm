function threeSum_v1(nums: number[]): number[][] {
  const result: number[][] = [];
  const n = nums.length;

  if (n < 3) return result;

  // 1. æ’åº
  nums.sort((a, b) => a - b);

  // 2. éå†ï¼Œå›ºå®šç¬¬ä¸€ä¸ªæ•°
  for (let i = 0; i < n - 2; i++) {
    // ä¼˜åŒ–ï¼šå¦‚æœæœ€å°çš„æ•°éƒ½ > 0ï¼Œä¸å¯èƒ½æœ‰è§£
    if (nums[i] > 0) break;

    // å»é‡ï¼šè·³è¿‡é‡å¤çš„ç¬¬ä¸€ä¸ªæ•°
    if (i > 0 && nums[i] === nums[i - 1]) continue;

    // 3. åŒæŒ‡é’ˆæ‰¾å‰©ä¸‹ä¸¤ä¸ªæ•°
    let left = i + 1;
    let right = n - 1;
    const target = -nums[i]; // éœ€è¦æ‰¾çš„ä¸¤æ•°ä¹‹å’Œ

    while (left < right) {
      const sum = nums[left] + nums[right];

      if (sum === target) {
        result.push([nums[i], nums[left], nums[right]]);

        // å»é‡ï¼šè·³è¿‡é‡å¤çš„ left
        while (left < right && nums[left] === nums[left + 1]) left++;
        // å»é‡ï¼šè·³è¿‡é‡å¤çš„ right
        while (left < right && nums[right] === nums[right - 1]) right--;

        left++;
        right--;
      } else if (sum < target) {
        left++;
      } else {
        right--;
      }
    }
  }

  return result;
}

