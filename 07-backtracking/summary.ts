// 4. 🧠 核心思维模型
// ============================================================

/**
 * 【回溯的本质】
 *
 * 回溯 = 穷举决策树 + 剪枝
 *
 * ```
 *                    []
 *          /         |         \
 *        选择1      选择2      选择3
 *       /   \      /   \      /   \
 *     ...   ...  ...   ...  ...   ...
 * ```
 *
 * 三个核心操作：
 * 1. 做选择：path.push() / used[i] = true
 * 2. 递归：backtrack()
 * 3. 撤销选择：path.pop() / used[i] = false
 *
 * 【去重的本质】
 *
 * 重复分为两种：
 * 1. 同一元素被重复使用 → used 数组
 * 2. 同层选择重复元素 → 排序 + 跳过
 *
 * ┌─────────────────────────────────────────────────────────────┐
 * │  【组合/子集去重】                                           │
 * │   if (i > start && nums[i] === nums[i-1]) continue;        │
 * │                                                             │
 * │  【排列去重】                                                │
 * │   if (i > 0 && nums[i] === nums[i-1] && !used[i-1])        │
 * │     continue;                                               │
 * └─────────────────────────────────────────────────────────────┘
 */

